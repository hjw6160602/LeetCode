import Foundation

class Solution {
    func lengthOfLIS(_ nums: [Int]) -> Int {
        guard nums.count > 0 else {
            return 0
        }
        
        return 0
    }
}

let nums = [10,9,2,5,3,7,101,18]
let s = Solution()
print(s.lengthOfLIS(nums))
